package bitwiseoparators;

public class C {
public static void main(String[] args) {
	System.out.println(Integer.parseInt("111111111",2));
	System.out.println(Integer.parseInt("000010110",2));
	System.out.println(Integer.parseInt("111101001",2));
	System.out.println(Integer.parseInt("0010011",2));
	
	System.out.println(342 | 191);
	System.out.println(342 & 191);
	System.out.println(342 ^ 191);
}
}
